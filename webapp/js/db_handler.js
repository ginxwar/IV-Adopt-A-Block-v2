var globalFormData;

 function db_post_image(formData)
{ 
 globalFormData = formData;
 var xhr = new XMLHttpRequest();
 if (xhr.upload) 
  {
    xhr.upload.addEventListener
      (     
       'progress',
       function (event)
         {
	      var percent = parseInt(event.loaded / event.total * 100);
	      progressElement.css("width", percent+'%'); //refer to handle_photo()
         }, 
         false                   
       );
    // File uploaded / failed
   xhr.onreadystatechange = function(event) 
   {
	if (xhr.readyState == 4) 
	 {
	  if (xhr.status == 200) 
	   { 
		var imageInfo=JSON.parse(xhr.responseText);
		if (imageInfo.error == 0)
		 {
		  formData
		  var imageUrl =  imageInfo.url;
		  var point =     imageInfo.point;
		  alert(point.lat+" "+point.lng+" "+point.epoch);
		  var comment =   imageInfo.comment;
		  var tripId =    imageInfo.tripId;
		  var imageType = imageInfo.imageType;
		  var type =      imageInfo.type;
		  globalFormData.append("url", imageUrl); 
		  console.log('photo uploaded at '+imageUrl+'. '+comment); 	
		  // now load on mongo the info
		  db_post_URLimage(imageUrl, point, comment, tripId, imageType, type);
		  //db_post_image_Kevin(globalFormData);						 
		 }
	    else 
	    {
		 alert (imageInfo.message);
		 console.log('photo not uploaded '+imageInfo.message); 						
	    }
	   }
	  else 
	   {
		alert ('something went wrong with the photo upload');
		console.log('photo is not uploaded'); 						
	   }
	  db_post_image_callback();  
	 }
    }
  }
 xhr.open('post', 'http://danielefani.altervista.org/cleanItUp/CITRIX/admin/loadPhoto.php', true);
 xhr.send(formData); 
}


 

